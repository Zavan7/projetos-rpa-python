bora = [23, 45, 67, 89, 12, 56, 78, 90, 34, 56]

tamanho_da_bomba = len(bora)
print(f'O tamanho dessa bomba é {tamanho_da_bomba}')

menor = min(bora)
print(f'O menor dessa bomba é {menor}')

maior = max(bora)
print(f'O maior dessa bomba é {maior}')

soma_da_bomba = sum(bora)
print(f'A soma dessa bomba é {soma_da_bomba}')

peso = [58.5, 63.2, 71.3, 69.4, 68.2]

media = sum(peso) / len(peso)
print (f'A média de peso é {media:.2f}')